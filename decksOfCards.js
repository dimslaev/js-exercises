// We have a random set of unonrdered cards.
// Each card is represented by a string containing the rank and suit, e.g. "KS" = King of Spades.
// How many full decks of cards can be made out these cards

function countFullDecks(A) {
  const obj = {};
  const values = [];

  A.forEach((card) => {
    if (!obj.hasOwnProperty(card)) obj[card] = 0;
    else obj[card]++;
  });

  Object.keys(obj).forEach((x) => {
    const val = obj[x];
    console.log(typeof val);
    values.push(val);
  });

  return Math.min(...values);
}

